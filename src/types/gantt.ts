/** Конфигурация диаграммы — годы, строки и параметры отображения. */
export interface GanttConfig {
        /** Список годов с подписью и массивом подписей месяцев. */
        years: { label: string; months: string[] }[];
        /** Подписи строк (виды работ). */
        rows: string[];
        /** Ширина одной ячейки (месяца) в пикселях. */
        cellWidth: number;
        /** Высота строки в пикселях. */
        rowHeight: number;
        /** Высота одной строки заголовка (годы / месяцы). Заголовок занимает headerHeight * 2. */
        headerHeight: number;
        /** Ширина колонки с подписями строк слева. */
        labelWidth: number;
}

/** Полоса на диаграмме Ганта. */
export interface GanttBar {
        /** Уникальный идентификатор. */
        id: string;
        /** Индекс строки (0-based). */
        rowIndex: number;
        /** X-координата левого края полосы в SVG-координатах. */
        startX: number;
        /** Ширина полосы в пикселях. */
        width: number;
        /** Цвет заливки/обводки в формате hex. */
        color: string;
        /** Текстовая метка, отображаемая по центру полосы. */
        label: string;
        /** Пунктирный стиль: без заливки, только обводка. */
        dashed: boolean;
}

/** Направленная связь между двумя полосами. */
export interface GanttConnection {
        /** Уникальный идентификатор. */
        id: string;
        /** ID полосы-источника. */
        fromBarId: string;
        /** Край источника: начало или конец полосы. */
        fromEdge: "start" | "end";
        /** ID полосы-назначения. */
        toBarId: string;
        /** Край назначения: начало или конец полосы. */
        toEdge: "start" | "end";
}